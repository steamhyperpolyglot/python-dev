# List Operations

print(len([5, "Omar", 3]))          # find the list length.
print([3, 4, 1] + ["Omar", 5, 6])   # concatenate lists
print(['Eg!'] * 4)                  # repeat an element in a list.
print(3 in [1, 2, 3])               # check if element in a list
for x in [1, 2, 3]:
    print(x, end=' ')               # traverse list elements